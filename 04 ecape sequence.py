# escape sequence character 
a= "hello my name is mukesh \n how are you"
print(a)
b="hello my name is mukesh \t how are you"
print(b)
c= "hello my name is mukesh \; how are you"
print(c)
d="hello my name is mukesh \\ how are you"
print(d)
e= "hello my name is mukesh \" how\" are you"
print(e)
f=" hello sir  \r chaudhary"
print(f)